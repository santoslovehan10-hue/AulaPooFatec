package Lista03;

// Imprimir os números de 1 a 10
public class Exercicio01 {
    public static void main(String[] args) {
        //Aqui foi feito um loop que começa em 1 e vai até 10, incrementando de 1 em 1. Aqui tem o menor ou igual a 10 e "++"
        for (int i = 1; i <= 10; i++) {
        // A cada repetição, imprime o valor atual de "i" no console
            System.out.println(i);
        }
    }
}